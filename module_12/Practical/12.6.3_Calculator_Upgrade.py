def digital_sum(num):
    total = 0
    while num > 0:
        total += num % 10 # –∑–∞–±–∏—Ä–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω—é—é —Ü–∏—Ñ—Ä—É —á–∏—Å–ª–∞ (–æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è –Ω–∞ 10) –∏ –ø—Ä–∏–±–∞–≤–ª—è–µ–º –µ–µ –≤ total
