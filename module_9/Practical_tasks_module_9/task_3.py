    answer = input(f'–ú–∞—Ä—Å–æ—Ö–æ–¥ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –Ω–∞ –ø–æ–∑–∏—Ü–∏–∏ {x}, {y}, –≤–≤–µ–¥–∏—Ç–µ –∫–æ–º–∞–Ω–¥—É: ')


