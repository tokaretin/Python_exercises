# Задача «Наибольший общий делитель»
# Наибольший общий делитель (НОД) — это самое большое число, на которое можно разделить два
# или более числа без остатка.
#
# Например, для чисел 12 и 15 это число 3, потому что и 12, и 15 можно разделить на 3 без
