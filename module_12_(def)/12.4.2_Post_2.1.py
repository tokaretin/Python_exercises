# фамилию, имя, страну проживания, город, улицу, номер дома и номер квартиры.
#
# Реализуйте функцию, которая получает все эти данные и выводит на экран.
# В программе вызовите функцию три раза с разными значениями аргументов.
#
#
# Подсказка: семь аргументов.

def post_name(sername, name, country, city, street, house, appartment):
    print(f'Фамилия: {sername}')
    print(f'Имя: {name}')
    print(f'Cтрана проживания: {country}')
    print(f'Город проживания: {city}')
    print(f'Улица проживания: {street}')
    print(f'Номер дома: {house}')
    print(f'Номер квартиры: {appartment} \n')

# Сохраняю данные всех людей
people_list = []

# Ввод данных 3-х человек
for people in range(1, 4):
    print(f'{people}-е данные человека: ')
    surname = input('Введите свою Фамилию: ')
    name = input('Введите свое Имя: ')
    country = input('Введите страну проживания: ')
    city = input('Введите город проживания: ')
    street = input('Введите улицу проживания: ')
    house = input('Введите номер дома: ')
    appartment = input('Введите номер квартиры: ')

    # Добавляю людей в список
    # person — это переменная, которая хранит данные одного человека в виде кортежа:
    # Cоздаём кортеж person, например: ('Иванов', 'Иван', 'Россия', 'Москва', 'Тверская', '84', '32')
    person = (surname, name, country, city, street, house, appartment)
    # И добавляем его в список people_list:
    # people_list.append(person)
    # append() — это метод списков, который добавляет один элемент в конец списка.
    people_list.append(person)
    # В итоге, если вы ввели 3 человека, people_list будет выглядеть вот так:
    # [
    #   ('Иванов', 'Иван', 'Россия', 'Москва', 'Тверская', '84', '32'),
    #   ('Петров', 'Пётр', 'Сербия', 'Нови-Сад', 'Мостарска', '7', '12'),
    #   ('Смит', 'Джон', 'Америка', 'Нью-Йорк', 'Бродвей', '112', '85')
    # ]


# Вывод всех людей
print('\nВсе введённые данные:\n')

# enumerate(people_list, 1)?
# enumerate() — это встроенная функция, которая позволяет перебирать
# элементы списка, одновременно получая номер элемента (счётчик) и сам элемент.
# people_list — это список кортежей, где каждый кортеж содержит данные одного человека.
# 1 — это начальное значение счётчика. То есть номера будут начинаться с 1, а не с 0.
for number, person in enumerate(people_list, 1):
    print('Так я увижу весь картеж')
    # enumerate(people_list, 1) даст:
    # number = 1, person = ('Иванов', 'Иван', 'Россия', 'Москва', 'Тверская', '84', '32')
    # number = 2, person = ('Смит', 'Джон', 'США', 'Нью-Йорк', 'Бродвей', '112', '85')
    print(f'{number}-й человек: {person}')

    # number — номер человека (1, 2, 3...).
    # person — кортеж с данными одного человека.
    # person = элемент списка people_list.

    # post_name(*person)?
    # *person — это распаковка кортежа. Она передаёт значения из кортежа
    # как отдельные аргументы в функцию post_name().
    post_name(*person)