# Триетий вариант
# Стартовая переменная
X = 1

# Получаем ввод как строку
user_input = input('Введите число в эксп. форме (Пример: 1e-3): ')

try:
    # Преобразуем в число (чтобы потом складывать)
    exp_N = float(user_input)

    # Проверяем, действительно ли формат содержит 'e'
    if 'e' in user_input.lower():
        # Разделяем строку на мантиссу и порядок
        mantissa_str, exponent_str = user_input.lower().split('e')
        mantissa = float(mantissa_str)
        exponent = int(exponent_str)

        # Проверка: мантисса от 1 до 9, порядок < 0
        if (1 <= abs(mantissa) <= 9) and (exponent < 0):
            count = 0
            while X <= 2:
                X += exp_N
                count += 1
            print(f'Кол-во прибавлений: {count}')
        else:
            print('❗ Мантисса должна быть от 1 до 9, а порядок — меньше 0.')
    else:
        print('❗ Введите число в экспоненциальной форме, например 1e-3.')

except ValueError:
    print('❗ Неверный формат числа. Введите, например: 1e-3')