# Задача 2. Чёрный ящик
# Преподаватель показал студентам несколько результатов
# программы и сказал: «Кто догадается, что делает программа
# и как она это делает, получит зачёт автоматом». Студент
# Миша намерен получить этот зачёт. Он заметил, что в результатах
# программы есть определённая закономерность. Вот некоторые результаты:
#
#
# Напишите программу, которая запрашивает у пользователя размер
# матрицы и выводит соответствующие результаты.
#
# Подсказка: столбцы.

N = int(input('Введите число: '))  # Ввод числа N — размер таблицы (N x N)

for row in range(1, N + 1):         # Внешний цикл проходит по строкам
    for col in range(1, N + 1):     # Внутренний цикл проходит по столбцам
        if col % 3 == 0:            # Если номер столбца делится на 3 без остатка
            print(col, end='\t')    # Выводим номер столбца
        else:
            print(row, end='\t')    # В остальных случаях выводим номер строки
    print('')                       # Переход на новую строку после заполнения строки

# Разберём на примере, если N = 5
